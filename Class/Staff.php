<?php


class Staff
{
    protected $id;
    protected $name;
    protected $phone;

    public function __construct($name,$phone)
    {
        $this->name = $name;
        $this->phone = $phone;
    }

    public function getName()
    {
        return $this->name;
    }

    public function setName($name)
    {
        $this->name = $name;
    }

    public function getPhone()
    {
        return $this->phone;
    }

    public function setPhone($phone)
    {
        $this->phone = $phone;
    }

    public function getId()
    {
        return $this->id;
    }

    public function setId($id)
    {
        $this->id = $id;
    }
}
